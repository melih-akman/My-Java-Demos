package condition_blocks;

public class ifEl {
    public static void main(String[] args) {
        int a = 5;
        if (a == 5) {
            System.out.println("a is equal to 5");
        } else if (a == 6) {
            System.out.println("a is equal to 6");
        } else {
            System.out.println("a is not equal to 5 or 6");
        }
    }
}
